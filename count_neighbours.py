def count_neighbours(grid, row, col):
    count_rows = len(grid)
    count_columns = len(grid[0])

    rows = [i for i in range(row - 1, row + 2) if i  >= 0 and i < count_rows]
    columns = [i for i in range(col - 1, col + 2) if i  >= 0 and i < count_columns]

    s = sum([grid[r][c] for r in rows for c in columns])

    return s - grid[row][col]


